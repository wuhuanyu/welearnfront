package com.example.stack.welearn.views;

import android.content.Context;
import android.support.annotation.Nullable;
import android.util.AttributeSet;
import android.widget.LinearLayout;

/**
 * Created by stack on 2018/1/4.
 */

public class SectionBar extends LinearLayout {
    public SectionBar(Context context) {
        super(context);
    }

    public SectionBar(Context context, @Nullable AttributeSet attrs) {
        super(context, attrs);
    }

    public SectionBar(Context context, @Nullable AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    public SectionBar(Context context, AttributeSet attrs, int defStyleAttr, int defStyleRes) {
        super(context, attrs, defStyleAttr, defStyleRes);
    }
}
